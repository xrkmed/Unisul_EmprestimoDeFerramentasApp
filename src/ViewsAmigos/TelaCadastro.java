    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        ArrayList<String> erros = new ArrayList<String>();

        if(!CEPResource.verificarNomeCompleto(textNome.getText())){
            erros.add("Nome inv√°lido, digite o nome completo (Ex: Jo√£o da Silva)");
            textNome.setText("");
            textNome.setEnabled(true);
        }

        if(!CEPResource.verificarCEP(textCEP.getText())){
            erros.add("CEP inv√°lido, digite apenas n√∫meros (Ex: 12345678)");
            textCEP.setText("");
            textCEP.setEnabled(true);
        }

        if(!CEPResource.verificarNumeroCasa(textNumero.getText())){
            erros.add("N√∫mero inv√°lido, digite apenas n√∫meros (Ex: 123)");
            textNumero.setText("");
            textNumero.setEnabled(true);
       }

        if(!CEPResource.verificarBairro(textBairro.getText())){
            erros.add("Bairro inv√°lido, digite apenas o nome do bairro (Ex: Jardim Paulista)");
            textBairro.setText("");
            textBairro.setEnabled(true);
        }

        if(!CEPResource.verificarCidade(textCidade.getText())){
            erros.add("Cidade inv√°lida, digite apenas o nome da cidade (Ex: S√£o Paulo)");
            textCidade.setText("");
            textCidade.setEnabled(true);
        }

        if(!CEPResource.verificarEstado(textEstado.getText())){
            erros.add("Estado inv√°lido, digite apenas a sigla do estado (Ex: SP)");
            textEstado.setText("");
            textEstado.setEnabled(true);
        }

        if(!CEPResource.verificarRua(textRua.getText())){
            erros.add("Rua inv√°lida, digite apenas o nome da rua (Ex: Rua Paulista)");
            textRua.setText("");
            textRua.setEnabled(true);
        }

        if(!PhoneValidResource.isValidPhoneNumber(PhoneValidResource.unformatPhoneNumber(textTelefone.getText()))){
            erros.add("Telefone inserido inv√°lido, digite apenas n√∫meros (Ex: 11912345678)");
            textTelefone.setText("");
            textTelefone.setEnabled(true);
        }

        if(erros.size() > 0){
            String mensagem = "";
            for(String erro : erros){
                mensagem += erro + "\n";
            }
            JOptionPane.showMessageDialog(null, "[+] Alguns erros foram encontrados: \n\n\n" + mensagem);
        }else{
            try{
                AddressResource address = new AddressResource(textRua.getText(), textBairro.getText(), textCidade.getText(), textEstado.getText(), Integer.parseInt(textCEP.getText()), textComplemento.getText(), Integer.parseInt(textNumero.getText()));
                FriendModel friendModel = new FriendModel(textNome.getText().toUpperCase(), address, PhoneValidResource.unformatPhoneNumber(textTelefone.getText()));
                FriendsDAO.getInstance().addFriend(friendModel);
                JOptionPane.showMessageDialog(null, "Amigo cadastrado com sucesso!");
                FriendsDAO.getInstance().logFriends();
                this.dispose();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

